package Lesson4;

public class MathMain {
    public static void main(String[] args) {
        //Методы класса Math
        System.out.println(java.lang.Math.max(2, 10)); //Max
        System.out.println(java.lang.Math.min(2, 10));  //Min
        System.out.println(java.lang.Math.PI); //Pi
        System.out.println(java.lang.Math.abs(-2)); //модуль
        System.out.println(java.lang.Math.abs(-2));
        System.out.println(java.lang.Math.ceil(2.3));  //округление вверх
        System.out.println(java.lang.Math.round(2.3)); //округленние
        System.out.println(java.lang.Math.floor(2.3)); //округление вниз
        System.out.println(java.lang.Math.sqrt(16));   //корень
        System.out.println(java.lang.Math.pow(16, 2));  //степень
        System.out.println(java.lang.Math.random());   //псевдо случайное число

    }
}
